def com_pos(m1, r1, m2, r2):
    return (m1 * r1 + m2 * r2) / (m1 + m2)


def com_vel(m1, v1, m2, v2):
    return (m1 * v1 + m2 * v2) / (m1 + m2)


def massa_no_tempo(t, m0, tau, massa_variavel):
    """
    Massa constante:
        m(t) = m0

    Massa variável:
        m(t) = m0 * exp(-t/tau)

    A função funciona tanto para t escalar quanto para t vetor.
    """

    t_array = np.asarray(t, dtype=float)

    if massa_variavel:
        massa = m0 * np.exp(-t_array / tau)
    else:
        massa = np.full_like(t_array, m0, dtype=float)

    if massa.ndim == 0:
        return float(massa)

    return massa


