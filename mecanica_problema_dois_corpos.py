def simular_dois_corpos_3d(
    nome_caso,
    G,
    m1,
    m2,
    eps,
    r1_0,
    r2_0,
    v1_0,
    v2_0,
    V_CM=None,
    massa_variavel=False,
    tau1=None,
    tau2=None,
    t_final=20.0,
    n_points=4000,
    skip=4,
    fps=30,
    dpi=200,
    salvar_video=True,
    mostrar_grafico_estatico=True
):
    print("\n======================================================")
    print(f"Rodando simulação: {nome_caso}")
    print("======================================================")

    if massa_variavel:
        if tau1 is None or tau2 is None:
            raise ValueError("Para massa variável, tau1 e tau2 precisam ser definidos.")

    r1_0 = np.array(r1_0, dtype=float)
    r2_0 = np.array(r2_0, dtype=float)
    v1_0 = np.array(v1_0, dtype=float)
    v2_0 = np.array(v2_0, dtype=float)

    if V_CM is None:
        V_CM = np.array([0.0, 0.0, 0.0], dtype=float)
    else:
        V_CM = np.array(V_CM, dtype=float)

    v1_0 = v1_0 + V_CM
    v2_0 = v2_0 + V_CM

    r_com_0 = com_pos(m1, r1_0, m2, r2_0)
    v_com_0 = com_vel(m1, v1_0, m2, v2_0)

    print("G  =", G)
    print("m1 inicial =", m1)
    print("m2 inicial =", m2)
    print("m1/m2 inicial =", m1 / m2)
    print("eps =", eps)
    print("massa_variavel =", massa_variavel)

    if massa_variavel:
        print("tau1 =", tau1)
        print("tau2 =", tau2)
        print("m1(t_final)/m1(0) =", np.exp(-t_final / tau1))
        print("m2(t_final)/m2(0) =", np.exp(-t_final / tau2))

    print("V_CM =", V_CM)
    print("r1(0) =", r1_0)
    print("r2(0) =", r2_0)
    print("v1(0) após V_CM =", v1_0)
    print("v2(0) após V_CM =", v2_0)
    print("Centro de massa inicial:", r_com_0)
    print("Velocidade inicial do centro de massa:", v_com_0)

    init_params = np.concatenate((r1_0, r2_0, v1_0, v2_0))

    time_span = np.linspace(0.0, t_final, n_points)

    sol_object = solve_ivp(
        two_body_eqn,
        [0.0, t_final],
        init_params,
        t_eval=time_span,
        args=(G, m1, m2, eps, massa_variavel, tau1, tau2),
        method="DOP853",
        rtol=1e-10,
        atol=1e-10
    )

    if not sol_object.success:
        print("A integração falhou:")
        print(sol_object.message)
        return None

    solution = sol_object.y.T

    r1_sol = solution[:, 0:3]
    r2_sol = solution[:, 3:6]
    v1_sol = solution[:, 6:9]
    v2_sol = solution[:, 9:12]

    m1_series = massa_no_tempo(time_span, m1, tau1, massa_variavel)
    m2_series = massa_no_tempo(time_span, m2, tau2, massa_variavel)

    r_com_sol = (
        m1_series[:, None] * r1_sol
        + m2_series[:, None] * r2_sol
    ) / (m1_series[:, None] + m2_series[:, None])

    E = energia_total(r1_sol, r2_sol, v1_sol, v2_sol, G, m1_series, m2_series, eps)
    P = momento_linear_total(v1_sol, v2_sol, m1_series, m2_series)
    L = momento_angular_total(r1_sol, r2_sol, v1_sol, v2_sol, m1_series, m2_series)

    E0 = E[0]
    Ef = E[-1]

    if abs(E0) > 1e-14:
        variacao_relativa_E = abs((Ef - E0) / E0)
    else:
        variacao_relativa_E = abs(Ef - E0)

    print("\n--- Grandezas físicas ---")
    print("Energia inicial:", E0)
    print("Energia final:  ", Ef)
    print("Variação relativa da energia:", variacao_relativa_E)
    print("Momento linear inicial:", P[0])
    print("Momento linear final:  ", P[-1])
    print("Variação do momento linear:", P[-1] - P[0])
    print("Momento angular inicial:", L[0])
    print("Momento angular final:  ", L[-1])
    print("Variação do momento angular:", L[-1] - L[0])

    if massa_variavel:
        print("\nObservação:")
        print("Como as massas variam com o tempo, energia, momento linear e momento angular")
        print("dos dois corpos restantes não precisam ser conservados.")

    # ========================================================
    # Gráfico estático 3D
    # ========================================================

    if mostrar_grafico_estatico:
        fig_static = plt.figure(figsize=(9, 8))
        ax_static = fig_static.add_subplot(111, projection="3d")

        ax_static.plot(r1_sol[:, 0], r1_sol[:, 1], r1_sol[:, 2], color="darkblue", label="Corpo 1")
        ax_static.plot(r2_sol[:, 0], r2_sol[:, 1], r2_sol[:, 2], color="red", label="Corpo 2")
        ax_static.plot(r_com_sol[:, 0], r_com_sol[:, 1], r_com_sol[:, 2], color="black", linestyle="--", label="Centro de massa")

        ax_static.scatter(r1_sol[0, 0], r1_sol[0, 1], r1_sol[0, 2], color="darkblue", s=80)
        ax_static.scatter(r2_sol[0, 0], r2_sol[0, 1], r2_sol[0, 2], color="red", s=80)
        ax_static.scatter(r_com_sol[0, 0], r_com_sol[0, 1], r_com_sol[0, 2], color="black", s=40)

        ax_static.set_xlabel("x")
        ax_static.set_ylabel("y")
        ax_static.set_zlabel("z")
        ax_static.set_title(nome_caso, pad=20)

        ax_static.view_init(elev=25, azim=35)
        ax_static.legend(loc="lower left")

        set_axes_equal_3d(ax_static, r1_sol, r2_sol, r_com_sol, fator_visual_z=0.75)

        plt.show()

    # ========================================================
    # Animação 3D
    # ========================================================

    r1_anim = r1_sol[::skip]
    r2_anim = r2_sol[::skip]
    r_com_anim = r_com_sol[::skip]
    time_anim = time_span[::skip]

    m1_anim = m1_series[::skip]

    m2_anim = m2_series[::skip]

    frames = len(r1_anim)

    fig = plt.figure(figsize=(9, 8))
    ax = fig.add_subplot(111, projection="3d")

    ax.set_xlabel("x")
    ax.set_ylabel("y")
    ax.set_zlabel("z")
    ax.set_title(nome_caso, pad=20)

    ax.view_init(elev=25, azim=35)

    set_axes_equal_3d(ax, r1_sol, r2_sol, r_com_sol, fator_visual_z=0.75)

    line1, = ax.plot([], [], [], color="darkblue", label="Corpo 1")
    line2, = ax.plot([], [], [], color="red", label="Corpo 2")
    line_com, = ax.plot([], [], [], color="black", linestyle="--", label="Centro de massa")

    point1, = ax.plot([], [], [], "o", color="darkblue", markersize=8)
    point2, = ax.plot([], [], [], "o", color="red", markersize=8)
    point_com, = ax.plot([], [], [], "o", color="black", markersize=5)

    ax.legend(loc="lower left")

    info_text = ax.text2D(
        0.98,
        0.95,
        "",
        transform=ax.transAxes,
        fontsize=10,
        ha="right",
        va="top",
        bbox=dict(facecolor='white', alpha=0.7, edgecolor='none', pad=5)
    )

    def init_animation():
        line1.set_data([], [])
        line1.set_3d_properties([])

        line2.set_data([], [])
        line2.set_3d_properties([])

        line_com.set_data([], [])
        line_com.set_3d_properties([])

        point1.set_data([], [])
        point1.set_3d_properties([])

        point2.set_data([], [])
        point2.set_3d_properties([])

        point_com.set_data([], [])
        point_com.set_3d_properties([])

        info_text.set_text("")

        return line1, line2, line_com, point1, point2, point_com, info_text

    def animate(i):
        line1.set_data(r1_anim[:i + 1, 0], r1_anim[:i + 1, 1])
        line1.set_3d_properties(r1_anim[:i + 1, 2])

        line2.set_data(r2_anim[:i + 1, 0], r2_anim[:i + 1, 1])
        line2.set_3d_properties(r2_anim[:i + 1, 2])

        line_com.set_data(r_com_anim[:i + 1, 0], r_com_anim[:i + 1, 1])
        line_com.set_3d_properties(r_com_anim[:i + 1, 2])

        point1.set_data([r1_anim[i, 0]], [r1_anim[i, 1]])
        point1.set_3d_properties([r1_anim[i, 2]])

        point2.set_data([r2_anim[i, 0]], [r2_anim[i, 1]])
        point2.set_3d_properties([r2_anim[i, 2]])

        point_com.set_data([r_com_anim[i, 0]], [r_com_anim[i, 1]])
        point_com.set_3d_properties([r_com_anim[i, 2]])

        if massa_variavel:
            info_text.set_text(
                f"t = {time_anim[i]:.2f}\n"
                f"Massa 1 restante (t/0) = {m1_anim[i] / m1:.3f}\n"
                f"Massa 2 restante (t/0) = {m2_anim[i] / m2:.3f}"
            )
        else:
            info_text.set_text(f"t = {time_anim[i]:.2f}")

        ax.view_init(elev=25, azim=35)

        return line1, line2, line_com, point1, point2, point_com, info_text

    anim = animation.FuncAnimation(
        fig,
        animate,
        init_func=init_animation,
        frames=frames,
        interval=20,
        blit=False
    )

    nome_video = nome_arquivo_seguro(nome_caso)

    if salvar_video:
        Writer = animation.writers["ffmpeg"]
        writer = Writer(fps=fps, metadata=dict(artist="MCQ"), bitrate=4000)

        anim.save(nome_video, writer=writer, dpi=dpi)

        print("\nVídeo salvo como:", nome_video)

    plt.close(fig)

    return {
        "time": time_span,
        "r1": r1_sol,
        "r2": r2_sol,
        "v1": v1_sol,
        "v2": v2_sol,
        "r_com": r_com_sol,
        "m1_t": m1_series,
        "m2_t": m2_series,
        "E": E,
        "P": P,
        "L": L,
        "video": nome_video
    }


