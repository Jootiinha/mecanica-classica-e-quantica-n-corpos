# Equações diferenciais do problema de dois corpos
# ============================================================

def two_body_eqn(t, w, G, m10, m20, eps, massa_variavel, tau1, tau2):
    r1 = w[0:3]
    r2 = w[3:6]
    v1 = w[6:9]
    v2 = w[9:12]

    m1_t = massa_no_tempo(t, m10, tau1, massa_variavel)
    m2_t = massa_no_tempo(t, m20, tau2, massa_variavel)

    r12 = r2 - r1

    dist2 = np.dot(r12, r12) + eps**2
    dist = np.sqrt(dist2)

    a1 = G * m2_t * r12 / dist**3
    a2 = -G * m1_t * r12 / dist**3

    derivs = np.concatenate((v1, v2, a1, a2))

    return derivs


